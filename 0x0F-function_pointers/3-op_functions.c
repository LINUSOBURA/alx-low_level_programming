#include "3-calc.h"
/**
 * op_add - funcion to add 2 integers
 * @a: first integer
 * @b: second integer
 * Return: a + b
 * Code by Linus Obura
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function to subtrsct two 2 integers
 * @a: first int
 * @b: second int
 * Return: a-b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function to multiply  2 ints
 * @a: first int
 * @b: second int
 * Return: a*b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function to devide 2 ints
 * @a: first int
 * @b: second int
 * Return: a/b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function to return mod
 * @a: first int
 * @b: second int
 * Return: a%b
 */
int op_mod(int a, int b)
{
	return (a % b);
}

